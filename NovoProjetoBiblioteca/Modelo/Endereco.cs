
namespace Semana08_Classes.Modelo
{
    /// <summary>
    /// Representa um endereço com informações básicas como bairro, cidade, complemento, estado, rua e número.
    /// </summary>
    public class Endereco
    {
        /// <summary>
        /// Bairro do endereço.
        /// </summary>
        string bairro;

        /// <summary>
        /// Cidade do endereço.
        /// </summary>
        string cidade;

        /// <summary>
        /// Complemento do endereço.
        /// </summary>
        string complemento;

        /// <summary>
        /// Estado do endereço.
        /// </summary>
        string estado;

        /// <summary>
        /// Rua do endereço.
        /// </summary>
        string rua;

        /// <summary>
        /// Número do endereço.
        /// </summary>
        int numero;
    }
}
